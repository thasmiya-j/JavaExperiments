
public class bitwise_operator_ {

	public static void main(String[] args) 
	{
		int a= 5,b = 6;
		int c = a&b;
		System.out.println("a&b is "+c);
		c = a|b;
		System.out.println("a|b is "+c);
		c = a^b;
		System.out.println("a&b is "+c);
		c = ~a;
		System.out.println("~a is "+c);
		c = a<<1;
		System.out.println("left shift of a  "+c);
		c = a>>1;
		System.out.println("right shift of a  "+c);
		
		
	}

}
